    // Botão de repor produtos no estoque:
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {
        {
            String codigo = JOptionPane.showInputDialog(this, "Digite o código do produto:");
            Produto p = produtos.buscarPorCodigo(codigo);

            if (p == null) {
                JOptionPane.showMessageDialog(this, "Produto não encontrado!");
                return;
            }

            int qtdAdicionar = Integer.parseInt(
                    JOptionPane.showInputDialog(this, "Digite a quantidade para repor:")
            );

            if (qtdAdicionar <= 0) {
                JOptionPane.showMessageDialog(this, "Quantidade inválida!");
                return;
            }

            p.setQuantidade(p.getQuantidade() + qtdAdicionar);
            JOptionPane.showMessageDialog(this, "Estoque atualizado! Novo estoque: " + p.getQuantidade());
        }

    }


    // Botão de vender um produto:
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {
        String codigo = JOptionPane.showInputDialog(this, "Digite o código do produto:");
        Produto p = produtos.buscarPorCodigo(codigo);

        if (p == null) {
            JOptionPane.showMessageDialog(this, "Produto não encontrado!");
            return;
        }

        int qtdVender = Integer.parseInt(
                JOptionPane.showInputDialog(this, "Digite a quantidade a vender:")
        );

        if (qtdVender <= 0) {
            JOptionPane.showMessageDialog(this, "Quantidade inválida!");
            return;
        }

        if (qtdVender > p.getQuantidade()) {
            JOptionPane.showMessageDialog(this, "Estoque insuficiente! Estoque atual: " + p.getQuantidade());
            return;
        }

        p.setQuantidade(p.getQuantidade() - qtdVender);
        JOptionPane.showMessageDialog(this,
                "Venda realizada!\nEstoque restante: " + p.getQuantidade());


        double valorVenda = qtdVender * p.getValorSaida();
        double valorBruto = qtdVender * p.getValorEntrada();
        double valorLucro = valorVenda - valorBruto;

        String txtRelatorioVenda = p.getCodigo() + ", " + p.getNome() + ", " + p.getMarca() + ", " + qtdVender + ", " + valorVenda
                + "R$" + "|| Lucro: " + valorLucro;

        relatorioVendas.adicionar(txtRelatorioVenda);
    }


